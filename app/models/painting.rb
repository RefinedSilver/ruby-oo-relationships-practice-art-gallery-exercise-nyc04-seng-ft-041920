class Painting

  attr_reader :title, :price

  @@all = []

  def initialize(title, price)
    @title = title
    @price = price
  end
  painting += total_price
  def most_expensive_painting
    @title
    @price
  end
  def all
    @@all
  end

end

`Painting.all`
    * Returns an `array` of all the paintings

  * `Painting.total_price`
    * Returns an `integer` that is the total price of all paintings
